class TrianglePattern:
    def setPattern(self,Board):
        Board.board[5][5] = 1
        Board.board[6][4] = 1
        Board.board[6][6] = 1
        Board.board[7][3] = 1
        Board.board[7][4] = 1
        Board.board[7][5] = 1
        Board.board[7][6] = 1
        Board.board[7][7] = 1
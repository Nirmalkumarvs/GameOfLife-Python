class SquarePattern:
    def setPattern(self, Board):
        Board.board[5][5] = 1
        Board.board[5][6] = 1
        Board.board[5][7] = 1
        Board.board[6][5] = 1
        Board.board[6][7] = 1
        Board.board[7][5] = 1
        Board.board[7][6] = 1
        Board.board[7][7] = 1